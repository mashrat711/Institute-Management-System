<?php

namespace App\Http\Controllers;

use App\ClassNew;
use App\Exam;
use App\Session;
use App\Student;
use App\Teacher;
use Illuminate\Http\Request;

class ExamController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $exams=Exam::all();
        return view('Backend.exams.index',compact('exams'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $teachersList = Teacher::pluck('name', 'id');
        $classes = ClassNew::pluck('class', 'id');
        $sections=Student::pluck('section', 'id');
        $sessions=Session::pluck('session', 'id');
        return view('Backend.exams.create',compact('teachersList','classes','sections','sessions'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data=$request->all();
        Exam::create($data);
        return redirect()->route('exams.index')->with('message','Data has been inserted successfully');
    }

    /**
